public class For {
    ////este es el metodo Main
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            System.out.println("Imprimo el valor de i: " +i);
        }
        System.out.println("=========");

        System.out.println("for decreciendo");

        for (int i = 10; i >= 1; i--) {

                System.out.println("Imprimo el valor de i: " +i );


        }
    }
}
